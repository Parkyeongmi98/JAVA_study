package chap_01;

public class _03_Variables {
    public static void main(String[] args) {
        // 변수 : 값을 저장하는 곳
        String name; // name이라는 문자열 변수
        name = "나도코딩";

        System.out.println(name + "님 배송이 시작됩니다. 15시에 방문 예정입니다.");
        System.out.println(name + "님, 배송이 완료되었습니다.");
    }
}
